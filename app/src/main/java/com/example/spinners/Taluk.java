package com.example.spinners;

public class Taluk {
    String Code;
    String Name;
    String KGISDistrictCode;
    String MSG;

    public String getMSG() {
        return MSG;
    }

    public void setMSG(String MSG) {
        this.MSG = MSG;
    }

    public String getCode() {
        return Code;
    }

    public void setCode(String code) {
        Code = code;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getKGISDistrictCode() {
        return KGISDistrictCode;
    }

    public void setKGISDistrictCode(String KGISDistrictCode) {
        this.KGISDistrictCode = KGISDistrictCode;
    }
}
